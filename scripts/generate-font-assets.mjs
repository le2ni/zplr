import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import opentype from "opentype.js";
import wawoff2 from "wawoff2";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const source = resolve(
  root,
  "node_modules/@fontsource-variable/noto-sans/files/noto-sans-latin-wdth-normal.woff2"
);
const target = resolve(root, "src/assets/notoSansCondensed.generated.ts");
const woff2 = readFileSync(source);
const ttf = await wawoff2.decompress(woff2);
const buffer = ttf.buffer.slice(ttf.byteOffset, ttf.byteOffset + ttf.byteLength);
const font = opentype.parse(buffer);
// Zebra Font 0 is CG Triumvirate Bold Condensed. Noto Sans' narrowest Latin
// instance at a bold weight is the closest redistributable deterministic
// substitute bundled by this project.
const coordinates = { wdth: 62.5, wght: 700 };
const transformedM = font.variation.getTransform(font.charToGlyph("M"), coordinates);
const mAdvance = transformedM.advanceWidth || font.unitsPerEm;
const zebraAdvanceScale = 0.77;
const advances = Array.from({ length: 256 }, (_, codePoint) => {
  const glyph = font.variation.getTransform(
    font.charToGlyph(String.fromCodePoint(codePoint)),
    coordinates
  );
  return Number(
    (((glyph.advanceWidth || mAdvance) / mAdvance) * zebraAdvanceScale).toFixed(6)
  );
});
const encoded = Buffer.from(ttf).toString("base64");
const chunks = encoded.match(/.{1,100}/g) ?? [];
const sourceHash = createHash("sha256").update(woff2).digest("hex");
const outputHash = createHash("sha256").update(ttf).digest("hex");

const output = `// Generated by scripts/generate-font-assets.mjs. Do not edit by hand.
// Noto Sans v42, 62.5% width / weight 700. See THIRD_PARTY_NOTICES.md.
export const NOTO_SANS_SOURCE_SHA256 = ${JSON.stringify(sourceHash)};
export const NOTO_SANS_TTF_SHA256 = ${JSON.stringify(outputHash)};
export const NOTO_SANS_VARIATION = Object.freeze({ wdth: 62.5, wght: 700 });
export const NOTO_SANS_ADVANCE_SCALE = ${zebraAdvanceScale};
export const NOTO_SANS_ADVANCE_RATIOS: readonly number[] = Object.freeze(${JSON.stringify(
  advances
)});
const NOTO_SANS_TTF_BASE64 = [
${chunks.map((chunk) => `  ${JSON.stringify(chunk)},`).join("\n")}
].join("");

let decoded: Uint8Array | undefined;

/** Returns the stable bundled Latin Font 0 outline bytes. */
export function notoSansCondensedTtf(): Uint8Array {
  if (!decoded) {
    const alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
    const bytes: number[] = [];
    let value = 0;
    let bits = 0;
    for (const character of NOTO_SANS_TTF_BASE64.replace(/=+$/, "")) {
      value = (value << 6) | alphabet.indexOf(character);
      bits += 6;
      if (bits >= 8) {
        bits -= 8;
        bytes.push((value >> bits) & 0xff);
      }
    }
    decoded = Uint8Array.from(bytes);
  }
  return decoded;
}
`;

mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, output);
