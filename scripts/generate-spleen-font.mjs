import { createHash } from "node:crypto";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const chunks = [];
for await (const chunk of process.stdin) chunks.push(chunk);
const source = Buffer.concat(chunks).toString("utf8");
if (!source.startsWith("STARTFONT")) throw new Error("Expected a BDF font on stdin.");

const glyphs = Object.create(null);
let encoding;
let bitmap;
for (const line of source.split(/\r?\n/)) {
  if (line.startsWith("ENCODING ")) encoding = Number(line.slice(9));
  else if (line === "BITMAP") bitmap = [];
  else if (line === "ENDCHAR") {
    if (encoding >= 32 && encoding <= 126 && bitmap?.length === 8) {
      glyphs[encoding] = bitmap;
    }
    encoding = undefined;
    bitmap = undefined;
  } else if (bitmap && /^[0-9A-Fa-f]{2}$/.test(line)) {
    bitmap.push(Number.parseInt(line, 16));
  }
}
if (Object.keys(glyphs).length !== 95) {
  throw new Error(`Expected 95 printable ASCII glyphs, got ${Object.keys(glyphs).length}.`);
}

const version = /Spleen 5x8 ([0-9.]+)/.exec(source)?.[1] ?? "unknown";
const hash = createHash("sha256").update(source).digest("hex");
const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const target = resolve(root, "src/assets/spleen5x8.generated.ts");
const output = `// Generated by scripts/generate-spleen-font.mjs. Do not edit by hand.
// Spleen 5x8 ${version}. See THIRD_PARTY_NOTICES.md.
export const SPLEEN_5X8_VERSION = ${JSON.stringify(version)};
export const SPLEEN_5X8_SOURCE_SHA256 = ${JSON.stringify(hash)};
export const SPLEEN_5X8_ROWS: Readonly<Record<number, readonly number[]>> = Object.freeze(${JSON.stringify(
  glyphs
)});
`;
mkdirSync(dirname(target), { recursive: true });
writeFileSync(target, output);
