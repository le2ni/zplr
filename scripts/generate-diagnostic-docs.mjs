import { readFile, readdir, writeFile } from "node:fs/promises";
import assert from "node:assert/strict";
import ts from "typescript";

const catalogUrl = new URL("../docs/diagnostic-codes.json", import.meta.url);
const targetUrl = new URL("../docs/DIAGNOSTICS.md", import.meta.url);
const catalog = JSON.parse(await readFile(catalogUrl, "utf8"));
const codes = catalog.map(({ code }) => code);
assert.equal(new Set(codes).size, codes.length, "Diagnostic codes must be unique.");
assert.deepEqual(codes, [...codes].sort(), "Diagnostic codes must remain sorted.");

const documented = new Set(codes);
const emittedPhases = new Map();

function diagnosticStrings(node) {
  const output = [];
  const visit = (current) => {
    if (
      ts.isStringLiteralLike(current) &&
      /^[A-Z][A-Z0-9_]{3,}$/.test(current.text)
    ) {
      output.push(current.text);
    }
    ts.forEachChild(current, visit);
  };
  visit(node);
  return output;
}

function record(code, phase, file) {
  assert.ok(documented.has(code), `${file} emits undocumented diagnostic ${code}.`);
  const phases = emittedPhases.get(code) ?? new Set();
  phases.add(phase);
  emittedPhases.set(code, phases);
}

for (const file of await readdir(new URL("../src/core/", import.meta.url))) {
  if (!file.endsWith(".ts") || file.endsWith(".test.ts")) continue;
  const source = await readFile(new URL(`../src/core/${file}`, import.meta.url), "utf8");
  const sourceFile = ts.createSourceFile(
    file,
    source,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TS
  );
  const visit = (node) => {
    if (ts.isCallExpression(node) || ts.isNewExpression(node)) {
      const name = ts.isIdentifier(node.expression)
        ? node.expression.text
        : undefined;
      const phase =
        name === "semanticDiagnostic" || name === "GraphicDecodeError"
          ? "semantic"
          : name === "renderDiagnostic"
          ? "render"
          : name === "diagnostic" && file === "documentParser.ts"
          ? "parse"
          : name === "diagnostic" && file === "rasterRenderer.ts"
          ? "render"
          : undefined;
      if (phase && node.arguments?.[0]) {
        for (const code of diagnosticStrings(node.arguments[0])) {
          record(code, phase, file);
        }
      }
    }
    if (ts.isObjectLiteralExpression(node)) {
      const property = (name) =>
        node.properties.find(
          (candidate) =>
            ts.isPropertyAssignment(candidate) &&
            ((ts.isIdentifier(candidate.name) && candidate.name.text === name) ||
              (ts.isStringLiteralLike(candidate.name) && candidate.name.text === name))
        );
      const codeProperty = property("code");
      const phaseProperty = property("phase");
      if (
        codeProperty &&
        phaseProperty &&
        ts.isPropertyAssignment(codeProperty) &&
        ts.isPropertyAssignment(phaseProperty) &&
        ts.isStringLiteralLike(phaseProperty.initializer)
      ) {
        for (const code of diagnosticStrings(codeProperty.initializer)) {
          record(code, phaseProperty.initializer.text, file);
        }
      }
    }
    ts.forEachChild(node, visit);
  };
  visit(sourceFile);
}

for (const { code, phase } of catalog) {
  const actual = [...(emittedPhases.get(code) ?? [])].sort();
  assert.deepEqual(
    actual,
    [phase],
    `${code} is cataloged as ${phase} but is emitted in ${actual.join(", ") || "no"} phase.`
  );
}

const rows = catalog.map(({ code, phase, meaning }) => `| \`${code}\` | ${phase} | ${meaning} |`);
const output = `<!-- Generated by scripts/generate-diagnostic-docs.mjs. Do not edit. -->
# Diagnostic codes

ZPLr returns syntax and semantic failures plus safety-limit violations as structured diagnostics. Parameters with documented default or ignore behavior can remain silent. Operational failures from a host Canvas adapter or user-supplied callback/provider reject the render promise. Applications should branch on \`code\`, not on English message text.

The codes below are stable for the 0.3 API freeze: a 0.3.x release may add a code, but it will not remove a code or reinterpret its meaning. Severity, phase, source span, related spans, command identity, and label index provide additional context.

| Code | Phase | Stable meaning |
| :--- | :--- | :--- |
${rows.join("\n")}
`;

if (process.argv.includes("--check")) {
  const current = await readFile(targetUrl, "utf8").catch(() => "");
  if (current !== output) {
    console.error("docs/DIAGNOSTICS.md is stale. Run pnpm docs:diagnostics.");
    process.exitCode = 1;
  }
} else {
  await writeFile(targetUrl, output);
}
